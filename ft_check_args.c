int	check_args(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] != '+' && str[i] != '-' &&
			       	(str[i] < '0' || str[i] > '9'))
			return (0);
		i++;
	}
	return (1);
}
